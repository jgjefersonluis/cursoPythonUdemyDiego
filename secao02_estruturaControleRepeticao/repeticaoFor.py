'''
Estruturas de Repetição - For
'''

# Mostrando de 1 até 10
for num in range(1, 11):
    print('Número{}'.format(num))

# Mostrando frases no terminal
for num in range(0, 5):
    print('Curso de Python')
    print('Estrutura de repetição')

# Mostrando de 10 até 1 no terminal, utilizando o passo -1
for num in range(10, 0, -1):
    print('Número{}'.format(num))

# Mostrando números pares, utilizando o passo 2
for num in range(0, 11, 2):
    print('Número {}'.format(num))






